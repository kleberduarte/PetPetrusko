    private void menOpcTrocaUsuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menOpcTrocaUsuActionPerformed
        // Metodo para trocar usuario
        int sair = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja trocar de usuário?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (sair == JOptionPane.YES_NO_OPTION) {
            this.dispose();
            {
                try {
                    // a linha abaixo fecha a conexão com o Banco de Dados
                    conexao.close();
                } catch (Exception e) {
                   JOptionPane.showMessageDialog(null, e);
                }
            }
            TelaLogin login = new TelaLogin();
            login.setVisible(true);

        }


    }//GEN-LAST:event_menOpcTrocaUsuActionPerformed

